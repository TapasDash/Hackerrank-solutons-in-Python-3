class Node:
    def __init__(self, info): 
        self.info = info  
        self.left = None  
        self.right = None 
        self.level = None 

    def __str__(self):
        return str(self.info) 

class BinarySearchTree:
    def __init__(self): 
        self.root = None

    def create(self, val):  
        if self.root == None:
            self.root = Node(val)
        else:
            current = self.root
         
            while True:
                if val < current.info:
                    if current.left:
                        current = current.left
                    else:
                        current.left = Node(val)
                        break
                elif val > current.info:
                    if current.right:
                        current = current.right
                    else:
                        current.right = Node(val)
                        break
                else:
                    break

"""
Node is defined as
self.left (the left child of the node)
self.right (the right child of the node)
self.info (the value of the node)
"""
def topView(root):
    nodes = [(root,0)]
    result = {}
    
    while len(nodes):
        tupleItem = nodes.pop(0)
        hd = tupleItem[1]
        t = tupleItem[0]        
        if hd not in result.keys():
            result[hd] = t.info
        if t.left:
            nodes.append((t.left,hd-1))
        if t.right:
            nodes.append((t.right,hd+1))
    for key in sorted(result): 
        print(result[key],end=" ")


tree = BinarySearchTree()
t = int(input())

arr = list(map(int, input().split()))

for i in range(t):
    tree.create(arr[i])

topView(tree.root)
